#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	while (n > 0)
	{
		n--;
		if (*ptr != (unsigned char)c)
			ptr++;
		else
			return (ptr);
	}
	return (NULL);
}
